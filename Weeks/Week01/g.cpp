// Math functions

#include <iostream>
#include <cmath> // for math functions

using namespace std;

int main() {

    int a;
    
    cin >> a;
    
    // Output
    cout << sqrt(a) << endl;
    cout << abs(a) << endl;
    cout << sin(a) << endl;
    cout << max(a, 5) << endl;
    cout << min(a, 5) << endl;
    cout << pow(a, 2) << endl;

    return 0;
}